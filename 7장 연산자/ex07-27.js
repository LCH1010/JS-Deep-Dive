// 논리합(||) 연산자
true || true; // true
true || false; // true
false || false; // true
false || false; // false

// 논리곱(&&) 연산자
true && true; // true
true && false; // false
false && false; // false
false && false; //  false

// 논리 부정 (!) 연산자
!true; // false
!false; // true

// 암묵적 타입 변환
!0; // true
!'Hello'; // true
